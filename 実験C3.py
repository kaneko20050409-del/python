# フーリエ変換
rect_fft = np.fft.fftshift(np.fft.fft(rect)) * dt
freqs = np.fft.fftshift(np.fft.fftfreq(N, d=dt))
S_in_fft = np.fft.fftshift(np.fft.fft(signal_in)) * dt

# --- 2. フィルタ処理と描画 ---
# 比較する帯域幅 W [Hz] のリスト
W_list = [20, 100, 500]
colors = ['orange', 'orange', 'orange']

fig, axes = plt.subplots(len(W_list), 2, figsize=(15, 12), constrained_layout=True)

for i, W in enumerate(W_list):
    # 理想LPF H(f) の適用
    H = np.where(np.abs(freqs) <= W / 2, 1.0 + 0.0j, 0.0 + 0.0j)
    S_out_fft = S_in_fft * H

    # 逆フーリエ変換 (時間領域へ戻す)
    signal_out = np.real(np.fft.ifft(np.fft.ifftshift(S_out_fft))) / dt

    # --- 左側：時間波形のプロット ---
    ax_t = axes[i, 0]
    ax_t.plot(t, signal_in, color='blue', alpha=0.7, label='Input (Rect+Noise)')
    ax_t.plot(t, rect, color='lightgreen', lw=1, label='Original Rect')
    ax_t.plot(t, signal_out, color=colors[i], lw=2, label=f'Filtered (W={W}Hz)')
    ax_t.set_xlim(-0.2, 0.2)
    ax_t.set_ylim(-1.5, 2.0)
    ax_t.set_title(f'Time Waveform (W = {W} Hz)')
    ax_t.set_xlabel('Time [s]')
    ax_t.set_ylabel('Amplitude')
    ax_t.legend(loc='upper right', fontsize='small')
    ax_t.grid(True, alpha=0.3)

    # --- 右側：振幅スペクトルのプロット ---
    ax_f = axes[i, 1]
    ax_f.plot(freqs, np.abs(S_in_fft), color='blue', alpha=0.7, label='Input Spectrum')
    ax_f.plot(freqs, np.abs(S_out_fft), color=colors[i], label='Filtered Spectrum')
    ax_f.plot(freqs, np.abs(rect_fft), color='green', lw=1, label='Original Rect')
    ax_f.set_xlim(-150, 150)
    ax_f.set_ylim(0, 0.12)
    ax_f.set_title(f'Amplitude Spectrum (W = {W} Hz)')
    ax_f.set_xlabel('Frequency [Hz]')
    ax_f.set_ylabel('Magnitude')
    ax_f.legend(loc='upper right', fontsize='small')
    ax_f.grid(True, alpha=0.3)

plt.show()