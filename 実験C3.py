# --- パラメータの設定 ---
# 1. 時間軸のパラメータ
fs = 10000       # サンプリング周波数 [Hz]
N = 100000       # サンプル数
dt = 1 / fs      # サンプリング間隔 [s]
t_start = -5.0   # 開始時間 [s]

# 時間配列 t を生成 [-5.0s, -4.9999s, ..., +4.9999s]
t = np.arange(N) * dt + t_start

# 2. 矩形信号 r(t) のパラメータ (式(1)に基づく)
tau = 0.1        # 時間幅 100 ms = 0.1 s
T0 = 0.0         # 中心時間

# --- 矩形信号データの生成 ---
# 定義式 r(t - T0) の |t - T0| <= tau / 2 の条件をNumPyの比較演算で実装
# True を 1.0 に、False を 0.0 に変換
rect_signal = (np.abs(t - T0) <= tau / 2).astype(float)

# --- グラフの描画 ---
plt.figure(figsize=(10, 8))

# グラフ2: 中心付近を拡大した様子 (パルス幅がわかるように)
plt.subplot(2, 1, 2) # 2行1列の2番目
# パルス幅を中心に、±0.2秒の範囲を拡大表示
plt.plot(t, rect_signal)
plt.title('r(t)(τ=100.0ms)')
plt.xlabel('Time [s]')
plt.ylabel('Amplitude')
plt.grid(True)
plt.xlim(T0 - 0.2, T0 + 0.2) # 中心±0.2秒の範囲
plt.ylim(-0.1, 1.1)

# 全体のレイアウトを調整
plt.tight_layout()
plt.show()

R_fft_raw = np.fft.fft(rect_signal)

# 連続時間フーリエ変換の解析解と合わせるためのスケーリング（重要！）
R_fft = R_fft_raw * dt

# 周波数軸を [0, fs] から [-fs/2, fs/2] に並べ替える
R_fft_shifted = np.fft.fftshift(R_fft)
freqs = np.fft.fftfreq(N, d=dt)
freqs_shifted = np.fft.fftshift(freqs)

# --- 5. グラフの描画 ---
plt.figure(figsize=(10, 5))

# 振幅絶対値スペクトルの比較
plt.plot(freqs_shifted, np.abs(R_fft_shifted), label='Numerical (FFT)', alpha=0.8)

