import numpy as np
import matplotlib.pylab as plt

sfreq = 10000 #サンプリング周波数(青点)
sfreq2 = 100000 #サンプリング周波数2(連続関数は表現できないためその代わりとして使う)
slen = 100000 #データ総数(slen/sfreq を計算すると何秒間のデータかがわかる)
f = 2000 #振動数(今回は１秒間に2000回振動する波を作っている)
A = np.sqrt(2)

t = np.arange(slen) / sfreq #0s～10sまで0.0001s間隔で区切ったndarray(多次元配列), slen個のデータが入る多次元配列
s = A * np.sin(2 * np.pi * f * t) #サンプリング(青点)の波の式 2πftの部分は(2π:1周あたりの角度,f:1秒間に何回転させたいか,t:経過時間)A:実行値を1.0にしたいなら波の振幅をroot2倍する必要がある
t2 = np.arange(slen) / sfreq2 #0s～1sまで0.00001s間隔で区切った多次元配列
s2 = A * np.sin(2 * np.pi * f * t2) #連続関数とみなす波の式
plt.plot(t * 1e3, s, 'o', label='Samples (sfreq = 10 kHz)') # t*1e3:x軸データ1000倍して単位をsからmsに変更している s:y軸データsの値が入っている 'o':フォーマット文字列今回は点を表示 label:ラベル
plt.plot(t2 * 1e3, s2, '--', label='Samples (sfreq = 100 kHz)') # '--':破線を表示
plt.legend(loc='upper right') #判例を表示するためのコード↑で指定したlabelの内容を出力locで位置を決めている
plt.xlim([0,2]) #x軸の表示範囲を制限するコード
plt.ylabel('Amplitude') #ラベルを表示
plt.xlabel('Time (ms)') #ラベルを表示
plt.show() #グラフ表示

intensity = s ** 2 #瞬時電力(信号の強さの二乗)
energy = (intensity * 1 / sfreq).sum() #全エネルギー(瞬時電力をtで積分、それを近似的に表している) 1/sfreq=dt,intensity*dtをsum
power = (energy) / (slen / sfreq) #平均電力(全エネルギーを時間で割っている)
power_theory = A ** 2 / 2 #理論的な平均電力(sinの平均電力は振幅の二乗/2となることが数学的に証明されている)
energy_theory = power_theory * slen / sfreq #理論的な全エネルギー(平均電力*時間)
ave_intensity = intensity.mean() #強度の時間平均
print('Energy: {}'.format(energy))
print('Power: {}'.format(power))
print('Energy (theory): {}'.format(energy_theory))
print('Power (theory): {}'.format(power_theory))
print('Average intensity: {}'.format(ave_intensity))

sf = np.fft.fft(s,norm = 'forward') #sをフーリエ変換した
ffreq = np.fft.fftfreq(slen, 1 / sfreq) #フーリエ変換したsの周波数軸
plt.plot(ffreq * 1e-3, np.abs(sf), label='Spectrum (sfreq = 10 kHz)')
plt.xlim([-3,3])
plt.xlabel('Frequency (kHz)')
plt.ylabel('Amplitude')
plt.legend()
plt.show()

fourier_average_intensity = np.mean(np.abs(sf)**2) * slen
print(f'Average intensity in frequency field: {fourier_average_intensity}')
